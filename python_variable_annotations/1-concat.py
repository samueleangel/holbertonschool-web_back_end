#!/usr/bin/env python3
"""
Script for a type-annotated function that concats two
values of type string
"""

def concat(str1: str, str2: str) -> str:
    """
    Function to sum (concat) the variables str1
    and str2.

    Args:
        str1 (string): First value type string
        str2 (string): Second value type string

    Returns:
        str1 + str2 = Concatenated string
    """
    return str1 + str2
